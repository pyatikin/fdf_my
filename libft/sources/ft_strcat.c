#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;

	i = ft_strlen(s1);
	s1[i] = *s2++;
	while (s1[i])
	{
		i++;
		s1[i] = *s2++;
	}
	return (s1);
}
